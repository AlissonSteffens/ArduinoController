    private void setComboBoxItens(){
        ArduinoConnectionController arduinoController = ArduinoConnectionController.getInstance();
        ports = arduinoController.getPortList();
        String[] strings = new String[ports.size()];
        for (int i = 0; i < ports.size(); i++) {
            strings[i]=ports.get(i);
        }
        ComboBoxModel<String> boxModel = new DefaultComboBoxModel<>(strings);
        jComboBox1.setModel(boxModel);
    }
    
