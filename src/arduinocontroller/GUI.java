    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        ArduinoConnectionController arduinoController = ArduinoConnectionController.getInstance();
        try {
            arduinoConnection = arduinoController.connectArduino(jComboBox1.getSelectedItem().toString());
            jPanel1.setVisible(true);
        } catch (SerialPortException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        if(jToggleButton1.isSelected()){
            try {
                arduinoConnection.digitalWrite((byte) 0x01);
            } catch (SerialPortException ex) {
                Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            try {
                arduinoConnection.digitalWrite((byte) 0x00);
            } catch (SerialPortException ex) {
                Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

